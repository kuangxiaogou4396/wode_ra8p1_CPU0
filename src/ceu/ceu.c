static bool IICWaitgtc(void)
    {
        unsigned short wTimeout = 10000;
        while((!g_i2c_transfer_complete)&&(!g_i2c_error_event)&& wTimeout)
        {
        	R_BSP_SoftwareDelay(1u, BSP_DELAY_UNITS_MICROSECONDS);
                wTimeout--;
        }
        g_i2c_transfer_complete = false;
        if((g_i2c_transfer_complete==false && wTimeout==0)||g_i2c_error_event)
                return false;
        return true;
    }
void i2c_write( uint16_t reg_addr, uint8_t *data, uint8_t len)
{
	    uint8_t buf[32];
	    buf[0] = (reg_addr >> 8) & 0xFF;   // 高 8 位
	    buf[1] = reg_addr & 0xFF;          // 低 8 位
	    for(uint8_t i=0; i<len; i++) buf[i+2] = data[i];

	    R_IIC_MASTER_SlaveAddressSet(&g_i2c_master1_ctrl, OV5640_DEVICE_ADDRESS, I2C_MASTER_ADDR_MODE_7BIT);

	    g_i2c_transfer_complete = false;
	    g_i2c_error_event = false;
	    R_IIC_MASTER_Write(&g_i2c_master1_ctrl, buf, len+2, false); // 注意长度 +2
	    IICWaitgtc();
}

uint8_t i2c_read( uint16_t reg_addr, uint8_t *data, uint32_t len)
{

	    uint8_t addr_buf[2];
	    addr_buf[0] = (reg_addr >> 8) & 0xFF;   // 高 8 位
	    addr_buf[1] = reg_addr & 0xFF;          // 低 8 位

	    R_IIC_MASTER_SlaveAddressSet(&g_i2c_master1_ctrl, OV5640_DEVICE_ADDRESS, I2C_MASTER_ADDR_MODE_7BIT);

	    g_i2c_transfer_complete = false;
	    g_i2c_error_event = false;
	    R_IIC_MASTER_Write(&g_i2c_master1_ctrl, addr_buf, 2, true); // 写完不停止，保持总线
	    IICWaitgtc();

	    g_i2c_transfer_complete = false;
	    g_i2c_error_event = false;
	    R_IIC_MASTER_Read(&g_i2c_master1_ctrl, data, len, false);
	    IICWaitgtc();
	    return data;
}
void OV5640_WriteReg(uint16_t reg, uint8_t data)
{
	i2c_write( reg, &data, 1);
}
uint8_t OV5640_ReadReg(uint16_t reg)
{
	uint8_t shuju;
	return i2c_read(reg,&shuju,1);
}
