uint8_t OV5640_WriteFW_FSP(uint8_t *pBuffer, uint16_t BufferSize)
{
    uint16_t addr = OV5640_FW_START_ADDR;
    uint8_t temp_buf[32];

    while(BufferSize)
    {
        uint16_t chunk = (BufferSize > 30) ? 30 : BufferSize; // 每次最多写30字节，前2字节给寄存器地址
        temp_buf[0] = (addr >> 8) & 0xFF; // 高8位
        temp_buf[1] = addr & 0xFF;        // 低8位

        for(uint16_t i = 0; i < chunk; i++)
        {
            temp_buf[i+2] = *pBuffer++;
        }

        g_i2c_transfer_complete = false;
        g_i2c_error_event = false;

        // 写数据，false表示写完后生成STOP
        if(R_IIC_MASTER_Write(&g_i2c_master1_ctrl, temp_buf, chunk + 2, false) != FSP_SUCCESS)
            return 0xFF;

        if(!IICWaitgtc())  // 等待传输完成
            return 0xFF;

        addr += chunk;
        BufferSize -= chunk;
    }


    return 0; // 成功
}
